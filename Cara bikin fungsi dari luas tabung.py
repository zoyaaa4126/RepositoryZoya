import math
def perkalian (jari, tinggi):
    hasil = math.pi * jari**2 * tinggi
    return hasil 

jari = float(input("Masukkan nilai jari-jari: "))
tinggi = float(input("Masukkan nilai tinggi: "))
volume= perkalian (jari, tinggi)
print("Volume tabung adalah: ", volume)

